package cn.itcast.chapter05.session.example02;

import java.awt.Color;
import java.awt.Font;
import java.awt.Graphics;
import java.awt.image.BufferedImage;
import java.io.ByteArrayOutputStream;
import java.io.IOException;

import javax.imageio.ImageIO;
import javax.servlet.ServletException;
import javax.servlet.ServletOutputStream;
import javax.servlet.annotation.WebServlet;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import javax.servlet.http.HttpSession;

@WebServlet(urlPatterns="/CheckServlet")
public class CheckServlet extends HttpServlet {
	private static int WIDTH=60;//验证码图片宽度
	private static int HEIGHT=20;//验证码图片高度
	@Override
	protected void doGet(HttpServletRequest req, HttpServletResponse resp) throws ServletException, IOException {
		// TODO Auto-generated method stub
		HttpSession session=req.getSession();
		resp.setCharacterEncoding("image/jpeg");
		resp.setContentType("text/html;charset=gbk");
		ServletOutputStream sos=resp.getOutputStream();
		//设置浏览器不要缓存图片
		resp.setHeader("Pragma", "No-cache");
		resp.setHeader("Cache-Control", "no-cache");
		resp.setDateHeader("Expires", 0);
		//创建内存图像并获得其图形上下文
		BufferedImage image=new BufferedImage(WIDTH,HEIGHT,BufferedImage.TYPE_INT_RGB);
		Graphics g=image.getGraphics();
		//产生随机码的认证码
		char[] rands=generateCheckCode1();
		//产生图像
		drawBackground(g);
		drawRands(g,rands);
		//结束图像的绘制过程，完成图像
		g.dispose();
		//将图像输出到客户端
		ByteArrayOutputStream bos=new ByteArrayOutputStream();
		ImageIO.write(image, "JPEG", bos);
		byte[] buf=bos.toByteArray();
		resp.setContentLength(buf.length);
		//下面的语句也可以写成：bos.writeTo(sos);
		sos.write(buf);
		bos.close();
		sos.close();
		//将当前验证码存入到Session中
		session.setAttribute("check_code", new String(rands));
		//直接使用下面的代码将有问题，Session对象必须在提交响应钱获得
		//req.getSession().setAttribute("check_code", new String(rands));
		
	}
	//生成一个4字符的验证码
	private char[] generateCheckCode1() {
		//定义验证码的字符表
		String chars="0123456789abcdefghijklmnopqrstuvwxyz";
		char[] rands=new char[4];
		for(int i=0;i<4;i++) {
			int rand=(int) (Math.random()*36);
			rands[i]=chars.charAt(rand);
		}
		return rands;
	}
	
	private void drawRands(Graphics g, char[] rands) {
		// TODO Auto-generated method stub
		g.setColor(Color.BLACK);
		g.setFont(new Font(null,Font.ITALIC|Font.BOLD,18));
		//在不同的高度上输出验证码的每个字符
		g.drawString(""+rands[0], 1, 17);
		g.drawString(""+rands[1], 1, 17);
		g.drawString(""+rands[2], 1, 17);
		g.drawString(""+rands[3], 1, 17);
		System.out.println(rands);
	}
	private void drawBackground(Graphics g) {
		// TODO Auto-generated method stub
		//画背景
		g.setColor(new Color(0xDCDCDC));
		g.fillRect(0, 0, WIDTH, HEIGHT);
		//随机产生120个干扰点
		for(int i=0;i<120;i++) {
			int x=(int) (Math.random()*WIDTH);
			int y=(int) (Math.random()*HEIGHT);
			int red=(int) (Math.random()*255);
			int green=(int) (Math.random()*255);
			int blue=(int) (Math.random()*255);
			g.setColor(new Color(red,green,blue));
			g.drawOval(x, y, 1, 0);
		}
	}
	
}
